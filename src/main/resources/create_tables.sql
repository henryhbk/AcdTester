  
    create table annotations (
       ROW_ID integer not null,
        ade_valid smallint,
        coment varchar(255),
        is_valid_drug smallint,
        noteevent_row_id integer,
        reaction_valid smallint,
        span_valid smallint,
        user_id integer,
        primary key (ROW_ID)
    )

    create table ADMISSIONS (
       ROW_ID integer not null,
        ADMISSION_LOCATION varchar(255),
        ADMISSION_TYPE varchar(255),
        ADMITTIME timestamp,
        DEATHTIME timestamp,
        DIAGNOSIS varchar(255),
        DISCHARGE_LOCATION varchar(255),
        DISCHTIME timestamp,
        EDOUTTIME timestamp,
        EDREGTIME timestamp,
        ETHNICITY varchar(255),
        HADM_ID integer,
        HAS_CHARTEVENTS_DATA Boolean,
        HOSPITAL_EXPIRE_FLAG Boolean,
        SUBJECT_ID varchar(255),
        primary key (ROW_ID)
    )
    
    create table CAREGIVERS (
       ROW_ID integer not null,
        CGID integer,
        description varchar(255),
        fhir_json varchar(255),
        first_name varchar(255),
        gender varchar(255),
        "LABEL" varchar(255),
        primary key (ROW_ID)
    )

    
    create table LABEVENTS (
       ROW_ID integer not null,
        acd_study smallint,
        charttime timestamp,
        fhir_json varchar(255),
        flag varchar(255),
        HADM_ID integer,
        itemid integer not null,
        SUBJECT_ID integer,
        value varchar(255),
        valuenum decimal(19,2),
        valueuom varchar(255),
        primary key (ROW_ID)
    )

    
    create table medication_adverse_event (
       id integer generated by default as identity,
        ADE_drug_name varchar(255),
        ADE_reaction varchar(255),
        ade_score float,
        ade_span varchar(255),
        start_index integer,
        end_index integer,
        ambiguous_phrase smallint,
        hadm_id integer,
        note_row_id integer,
        rx_norm_id varchar(255),
        ade_valid smallint,
        primary key (id)
    )

    
    create table NOTEEVENTS (
       ROW_ID integer not null,
        acd_study_note smallint,
        category varchar(255),
        CGID integer,
        chartdate date,
        DESCRIPTION varchar(255),
        HADM_ID integer,
        SUBJECT_ID varchar(255),
        TEXT varchar(255),
        primary key (ROW_ID)
    )

    
    create table PRESCRIPTIONS (
       ROW_ID integer not null,
        acd_study_med smallint,
        DOSE_UNIT_RX varchar(255),
        DOSE_VAL_RX varchar(255),
        drug varchar(255),
        DRUG_NAME_GENERIC varchar(255),
        DRUG_NAME_POE varchar(255),
        DRUG_TYPE varchar(255),
        enddate timestamp,
        fhir_json varchar(255),
        FORM_UNIT_DISP varchar(255),
        FORM_VAL_DISP varchar(255),
        FORMULARY_DRUG_CD varchar(255),
        GSN varchar(255),
        HADM_ID integer,
        ICUSTAY_ID integer,
        NDC varchar(255),
        PROD_STRENGTH varchar(255),
        ROUTE varchar(255),
        rxNormId varchar(255),
        startdate timestamp,
        SUBJECT_ID integer,
        primary key (ROW_ID)
    )

    
    create table social_history (
       subject_id integer not null,
        description varchar(255),
        hadm_id integer,
        text varchar(255),
        primary key (subject_id)
    )